#!/bin/bash
#PROGRAMA DE CALCULO
PRIMEIRO=4
SEGUNDO=2

#ADICIONAR
echo "Adicionar:"
echo $((PRIMEIRO+SEGUNDO))

#SUBTRAIR
echo "Subtrair:"
echo $((PRIMEIRO-SEGUNDO))

#MULTIPLICAR
echo "Multiplicar:"
echo $((PRIMEIRO*SEGUNDO))

#DIVIDIR
echo "DIVIDIR:"
echo $((PRIMEIRO/SEGUNDO))

echo "Arch Is Here!"
